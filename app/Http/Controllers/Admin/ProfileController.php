     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $admin = Admin::find($id);
        $admin->name = $request->get('name');
        $admin->email = $request->get('email');
        $admin->tel = $request->get('tel');
        $admin->address = $request->get('address');
        $admin->update();
        if (!$admin) {
            return $this->responseRedirectBack('Error occurred while updating admin information.', 'error', true, true);
        }
        return $this->responseRedirect('admin.profiles.index', 'Admin Information updating successfully', 'success', false, false);
    }

